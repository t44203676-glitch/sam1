            <h3 style="font-size: 18px; font-weight: bold; margin-bottom: 10px;">من نحن</h3>
            <p style="margin-bottom: 20px; font-size: 14px;">نظراً إلى أهمية ما تضطلع به وزارة الداخلية من مهام ومسؤوليات مختلفة تقتضي تطوير المنظومة وتحسينها بشكل مستمر بما يتناسب مع تطلعات القيادة، ومواكبة رؤية 2030 والتطلعات المحلية والعالمية في مجال الأمن والسلامة وتقديم الخدمات، ومن هنا تأسس برنامج تطوير وزارة الداخلية بموجب أمر ملكي صادر بتاريخ 14 ربيع الثاني 1438 هـ ليكون برنامجاً شاملاً لتطوير أعمال وعمليات منظومة وزارة الداخلية من أجل تحقيق منظومة عمل مؤسسي متميز يستند لأفضل الخبرات والممارسات والتجارب في المؤسسات المحلية والعالمية.</p>
            
            <h3 style="font-size: 18px; font-weight: bold; margin-bottom: 10px;">طموحنا</h3>
